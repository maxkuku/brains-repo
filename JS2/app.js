// 5. Реализовать функцию с тремя параметрами: function mathOperation(arg1, arg2, operation),
// где arg1, arg2 – значения аргументов, operation – строка с названием операции. В зависимости от
// переданного значения операции (использовать switch) выполнить одну из арифметических
// операций
// (использовать функции из задания 4) и вернуть полученное значение.


function mathOperation(arg1, arg2, operation) {
    // если указать let, то вылетает ошибка
    // Uncaught SyntaxError: Identifier 'arg1' has already been declared
    var arg1 = parseInt(prompt('Введите число a'));
    var arg2 = parseInt(prompt('Теперь число b'));
    var operation = prompt('Теперь арифметическую операцию: сложить - a, вычесть - s, умножить - m, разделить - d');
    if(Boolean(operation)){
        switch (operation){
            case "a":
                sums(arg1, arg2);
                break;
            case "s":
                subtr(arg1, arg2);
                break;    
            case "m":
                mult(arg1, arg2);
                break;
            case "d":
                divi(arg1, arg2);
                break;
            default:
                alert('Введите 2 числа и арифметическую операцию: сложить - a, вычесть - s, умножить - m, разделить - d');  
                mathOperation();
        }
    } else {
        console.log('Выход из программы');
        return;
    }
}
