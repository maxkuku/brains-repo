// 6. Программа вывода слова рубль в верном падеже

function padez() {
    let input = prompt('Сколько рублей вы хотите положить на счет?');
    let inputNum = parseInt(input);
    let arrayNumbers = [2, 3, 4];
    let inp = inputNum % 10;
    let result = (arrayNumbers.includes(inp) && inp < 10 ) ? "рубля" : (inp === 1 ? "рубль" : "рублей");
    alert(`Вы хотите вложить ${inputNum} ${result}`);
}

